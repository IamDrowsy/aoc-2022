export function isValidChar(s: string): s is Char {
  return /[a-zA-Z]/.test(s);
}

export function singleStringToChar(s: string) {
  if (isValidChar(s)) {
    return s;
  } else {
    throw new Error(`Invalid Char ${s}`);
  }
}

export function stringToChars(s: string) {
  return s.split("").map(singleStringToChar);
}

